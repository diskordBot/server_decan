# data/groups.py
"""Файл с данными о группах для всего приложения"""

DEFAULT_GROUPS = [
    'КИ-25', 'СП-25а', 'СП-25б', 'КСЦ-25', 'ПИ-25а', 'ПИ-25б', 'ПИ-25в',
    'ИИ-25а', 'ИИ-25б', 'ИНФ-25', 'САУ-25', 'ПМКИ-25', 'КИ-24', 'СП-24',
    'КСЦ-24', 'ПИ-24а', 'ПИ-24б', 'ИИ-24', 'ИНФ-24', 'САУ-24'
]

# Дополнительные группы для разных факультетов
FACULTY_GROUPS = {
    'ИСП': [
        'КИ-25', 'СП-25а', 'СП-25б', 'КСЦ-25', 'ПИ-25а', 'ПИ-25б', 'ПИ-25в',
        'ИИ-25а', 'ИИ-25б', 'ИНФ-25', 'САУ-25', 'ПМКИ-25'
    ],
    'Другие': [
        'КИ-24', 'СП-24', 'КСЦ-24', 'ПИ-24а', 'ПИ-24б', 'ИИ-24', 'ИНФ-24', 'САУ-24'
    ]
}

# Группы по курсам
GROUPS_BY_YEAR = {
    2025: ['КИ-25', 'СП-25а', 'СП-25б', 'КСЦ-25', 'ПИ-25а', 'ПИ-25б', 'ПИ-25в', 'ИИ-25а', 'ИИ-25б', 'ИНФ-25', 'САУ-25', 'ПМКИ-25'],
    2024: ['КИ-24', 'СП-24', 'КСЦ-24', 'ПИ-24а', 'ПИ-24б', 'ИИ-24', 'ИНФ-24', 'САУ-24']
}

def get_all_groups():
    """Возвращает все группы"""
    return DEFAULT_GROUPS

def get_groups_by_faculty(faculty):
    """Возвращает группы по факультету"""
    return FACULTY_GROUPS.get(faculty, [])

def get_groups_by_year(year):
    """Возвращает группы по году поступления"""
    return GROUPS_BY_YEAR.get(year, [])

def get_group_info(group_name):
    """Возвращает информацию о группе"""
    if group_name in DEFAULT_GROUPS:
        year = int(group_name.split('-')[1][:2]) + 2000
        return {
            'name': group_name,
            'year': year,
            'faculty': 'ИСП' if any(g.startswith(group_name.split('-')[0]) for g in FACULTY_GROUPS['ИСП']) else 'Другие'
        }
    return None